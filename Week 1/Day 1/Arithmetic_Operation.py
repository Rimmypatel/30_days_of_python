#Arithmetic Operation
# Operation           Name
#     +             addition
#     -             substraction
#     *             multiplication
#     /             division
#     %             modulus
#     **            exponential
#     //            floor division

num1 = 10
num2 = 3

result = num1+num2
print("addition")
print(result)

result = num1-num2
print("substraction")
print(result)

result = num1*num2
print("multiplication")
print(result)

result = num1/num2
print("division")
print(result)

result = num1%num2
print("modulus")
print(result)

result= num1 ** num2
print("exponential")
print(result)

result = num1//num2
print("floor Division")
print(result)